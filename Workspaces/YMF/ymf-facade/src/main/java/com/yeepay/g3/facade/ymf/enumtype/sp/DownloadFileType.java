package com.yeepay.g3.facade.ymf.enumtype.sp;

/**
 * 下载文件类型
 * Created by chen.liu on 2016/8/29.
 */
public enum DownloadFileType {

    TEXT("TXT文件"),

    EXCEL("EXCEL"),

    CSV("CSV");

    String displayName;

    DownloadFileType(String displayName) {
        this.displayName = displayName;
    }

    public String getDisplayName() {
        return displayName;
    }
}
