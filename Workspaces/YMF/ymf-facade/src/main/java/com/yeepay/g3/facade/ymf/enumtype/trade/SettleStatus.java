package com.yeepay.g3.facade.ymf.enumtype.trade;

/**
 * 扫码支付，订单清算状态
 * @author xiaobin.liu
 *
 */
public enum SettleStatus {
	 /**
     * 初始化
     */
	 INIT("未结算"),
	/**
	 * 清算
	 */
	SETTLED("已结算");

	private String displayName;

	SettleStatus(String displayName) {
		this.displayName = displayName;
	}

	public String getDisplayName() {
		return displayName;
	}

}
