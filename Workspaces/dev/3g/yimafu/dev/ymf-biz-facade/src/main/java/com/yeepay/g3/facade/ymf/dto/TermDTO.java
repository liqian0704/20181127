package com.yeepay.g3.facade.ymf.dto;

import com.yeepay.g3.facade.ymf.enumtype.term.TermOptType;

import java.io.Serializable;
import java.util.Date;

/**
 * Title: 终端DTO
 * Description:
 * Copyright: Copyright (c)2015
 * Company: YeePay
 *
 * @author chen.liu on 17/3/10.
 */
public class TermDTO implements Serializable {

    private static final long serialVersionUID = 8258631836695148934L;

    /**
     * 终端号
     */
    private String serial;

    /**
     * 终端类型
     */
    private String termType;

    /**
     * 终端厂商
     */
    private String manufacture;

    /**
     * 操作类型
     */
    private TermOptType operateType;

    /**
     * 操作时间
     */
    private Date createTime;

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public String getTermType() {
        return termType;
    }

    public void setTermType(String termType) {
        this.termType = termType;
    }

    public String getManufacture() {
        return manufacture;
    }

    public void setManufacture(String manufacture) {
        this.manufacture = manufacture;
    }

    public TermOptType getOperateType() {
        return operateType;
    }

    public void setOperateType(TermOptType operateType) {
        this.operateType = operateType;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    @Override
    public String toString() {
        return "TermDTO{" +
                "serial='" + serial + '\'' +
                ", termType='" + termType + '\'' +
                ", manufacture='" + manufacture + '\'' +
                ", operateType=" + operateType +
                ", createTime=" + createTime +
                '}';
    }
}
