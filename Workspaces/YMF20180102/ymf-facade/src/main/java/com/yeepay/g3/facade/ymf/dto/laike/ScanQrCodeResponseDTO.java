public class ScanQrCodeResponseDTO extends BaseScanCodeDTO {

    private static final long serialVersionUID = -6724996499505641755L;

    /**
     * 交易参考号,ymf系统唯一
     */
    private String externalID;
    /**
     * 扫码url,使用工具生成二维码供扫码使用
     */
    private String payUrl;
    /**
     * 交易响应码
     */
    private TrxCode trxCode;
    private String returnCode;
    private String returnMsg;

    public String getExternalID() {
        return externalID;
    }

    public void setExternalID(String externalID) {
        this.externalID = externalID;
    }

    public String getPayUrl() {
        return payUrl;
    }

    public void setPayUrl(String payUrl) {
        this.payUrl = payUrl;
    }

    public TrxCode getTrxCode() {
        return trxCode;
    }

    public void setTrxCode(TrxCode trxCode) {
        this.trxCode = trxCode;
    }

    public String getReturnCode() {
        return returnCode;
    }

    public void setReturnCode(String returnCode) {
        this.returnCode = returnCode;
    }

    public String getReturnMsg() {
        return returnMsg;
    }

    public void setReturnMsg(String returnMsg) {
        this.returnMsg = returnMsg;
    }

}
