package com.yeepay.g3.facade.ymf.dto.profit;

import com.yeepay.g3.facade.ymf.enumtype.profit.ProfitProductTypeEnum;

import java.math.BigDecimal;
import java.util.Date;

/**
 * @description: 毛利信息
 * @author: xiaobin.liu
 * @date: 17/12/14
 * @time: 下午2:37
 */
public class ProfitDTO {

    /**
     * 日期YYYYMM
     */
    private Date date;
    /**
     * 销售
     */
    private String sale;
    /**
     * 易宝商编
     */
    private String customerNumber;
    /**
     * 代理商商编
     */
    private String agentNumber;
    /**
     * 交易笔数
     */
    private Long trxCount;
    /**
     * 交易额
     */
    private BigDecimal trxAmt;
    /**
     * 毛利
     */
    private BigDecimal profitAmt;
    /**
     * 营销产品码
     */
    private String marketingCode;
    /**
     * 核算产品码
     */
    private String calculateCode;
    /**
     * 毛利产品
     */
    private ProfitProductTypeEnum profitProductType;
    /**
     * 蜜糖交易金额
     */
    private BigDecimal mitangTrxAmt;
    /**
     * 蜜糖毛利金额
     */
    private BigDecimal mitangProfitAmt;
    /**
     * 计算比例
     */
    private BigDecimal percent ;

    public BigDecimal getPercent() {
        return percent;
    }

    public void setPercent(BigDecimal percent) {
        this.percent = percent;
    }

    public BigDecimal getMitangTrxAmt() {
        return mitangTrxAmt;
    }

    public void setMitangTrxAmt(BigDecimal mitangTrxAmt) {
        this.mitangTrxAmt = mitangTrxAmt;
    }

    public BigDecimal getMitangProfitAmt() {
        return mitangProfitAmt;
    }

    public void setMitangProfitAmt(BigDecimal mitangProfitAmt) {
        this.mitangProfitAmt = mitangProfitAmt;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getSale() {
        return sale;
    }

    public void setSale(String sale) {
        this.sale = sale;
    }

    public String getCustomerNumber() {
        return customerNumber;
    }

    public void setCustomerNumber(String customerNumber) {
        this.customerNumber = customerNumber;
    }

    public String getAgentNumber() {
        return agentNumber;
    }

    public void setAgentNumber(String agentNumber) {
        this.agentNumber = agentNumber;
    }

    public Long getTrxCount() {
        return trxCount;
    }

    public void setTrxCount(Long trxCount) {
        this.trxCount = trxCount;
    }

    public BigDecimal getTrxAmt() {
        return trxAmt;
    }

    public void setTrxAmt(BigDecimal trxAmt) {
        this.trxAmt = trxAmt;
    }

    public BigDecimal getProfitAmt() {
        return profitAmt;
    }

    public void setProfitAmt(BigDecimal profitAmt) {
        this.profitAmt = profitAmt;
    }

    public String getMarketingCode() {
        return marketingCode;
    }

    public void setMarketingCode(String marketingCode) {
        this.marketingCode = marketingCode;
    }

    public String getCalculateCode() {
        return calculateCode;
    }

    public void setCalculateCode(String calculateCode) {
        this.calculateCode = calculateCode;
    }

    public ProfitProductTypeEnum getProfitProductType() {
        return profitProductType;
    }

    public void setProfitProductType(ProfitProductTypeEnum profitProductType) {
        this.profitProductType = profitProductType;
    }

    @Override
    public String toString() {
        return "ProfitDTO{" +
                "date=" + date +
                ", sale='" + sale + '\'' +
                ", customerNumber='" + customerNumber + '\'' +
                ", agentNumber='" + agentNumber + '\'' +
                ", trxCount=" + trxCount +
                ", trxAmt=" + trxAmt +
                ", profitAmt=" + profitAmt +
                ", marketingCode='" + marketingCode + '\'' +
                ", calculateCode='" + calculateCode + '\'' +
                '}';
    }
}
