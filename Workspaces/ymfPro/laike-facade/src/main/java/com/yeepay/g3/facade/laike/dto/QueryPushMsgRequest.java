package com.yeepay.g3.facade.laike.dto;

import com.yeepay.g3.facade.laike.enumtype.MsgTypeEnum;

/**
 * @Description: 消息查询请求
 * @Author: zhaoyu.cui
 * @Date: 16/11/10
 * @Time: 下午2:46
 */
public class QueryPushMsgRequest extends QueryBaseRequest {

	private MsgTypeEnum type;

	public MsgTypeEnum getType() {
		return type;
	}

	public void setType(MsgTypeEnum type) {
		this.type = type;
	}

	@Override
	public String toString() {
		final StringBuffer sb = new StringBuffer("QueryPushMsgRequest{");
		sb.append("memberNo='").append(memberNo).append('\'');
		sb.append("imei='").append(imei).append('\'');
		sb.append("versionId='").append(versionId).append('\'');
		sb.append("location='").append(location).append('\'');
		sb.append("type='").append(type).append('\'');
		sb.append('}');
		return sb.toString();
	}
}
