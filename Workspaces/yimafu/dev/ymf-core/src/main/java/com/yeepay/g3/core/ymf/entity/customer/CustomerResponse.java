package com.yeepay.g3.core.ymf.entity.customer;

import java.util.List;
import java.util.Map;

/**
 * 返回商户信息以及商户对应二维码版本
 * Created by dongxulu on 16/11/22.
 */

public class CustomerResponse {

    public Customer customer;

    public List<String> qrBusiness;

    public Map<String,String> businessMap;

    public String msg;

    public String getMsg() {
        return msg;
    }

    public Map<String, String> getBusinessMap() {
        return businessMap;
    }

    public void setBusinessMap(Map<String, String> businessMap) {
        this.businessMap = businessMap;
    }
    public void setMsg(String msg) {
        this.msg = msg;
    }


    public List<String> getQrBusiness() {
        return qrBusiness;
    }

    public void setQrBusiness(List<String> qrBusiness) {
        this.qrBusiness = qrBusiness;
    }


    public Customer getCustomer() {
        return customer;
    }

    public void setCustomer(Customer customer) {
        this.customer = customer;
    }


}
