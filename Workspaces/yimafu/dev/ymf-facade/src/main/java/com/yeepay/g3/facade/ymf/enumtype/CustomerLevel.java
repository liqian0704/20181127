public enum CustomerLevel {
    /**
     * 甲级
     */
    A("甲级"),
    /**
     * 乙级
     */
    B("乙级"),
    /**
     * 丙级
     */
    C("丙级");

    public String displayName;

    CustomerLevel(String displayName) {
        this.displayName = displayName;
    }

    public String getDisplayName() {
        return displayName;
    }
}
