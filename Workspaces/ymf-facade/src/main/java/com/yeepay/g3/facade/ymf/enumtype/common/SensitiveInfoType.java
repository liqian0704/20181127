public enum SensitiveInfoType {

    EMAIL("邮箱"),
    MOBILE("手机号"),
    PHONENUMBER("座机号"),
    IDCARD("身份证"),
    BANKCARD("银行卡号"),
    NAME("姓名");

    private String displayName;

    SensitiveInfoType(String displayName) {
        this.displayName = displayName;
    }

    public String getDisplayName() {
        return displayName;
    }


}
